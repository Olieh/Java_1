package sintaxe_basica;

public class Ab_Variaveis_Inteiros {

	public static void main(String[] args) {
//		declarando variaavel idade do tipo inteiro
		int idade;
//		definindo valor 37 para a variavel idade
		idade = 37;
//		imprimindo o valor da variavel idade
		System.out.println("idade é " + idade);
		
//		substituindo o valor da variavel idade pelo resultado da soma de 30 + 10
		idade = 30 + 10;
//		imprimindo o novo valor da variavel idade
		System.out.println("idade é " + idade);
	}
}
