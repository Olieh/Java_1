package sintaxe_basica;

public class Ae_char_e_String {
	public static void main(String[] args) {
		
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 66;
		System.out.println(valor);
		
		valor = (char) (valor+1);
		System.out.println(valor);
		
		String frase = "Uma palavra mais duas palavras é igual a três palavras";
		System.out.println(frase);
		
		frase = frase + 2000;
		System.out.println(frase);
		
	}
}
