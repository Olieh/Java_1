package sintaxe_basica;

public class Af_char_e_String_treinando {

	public static void main(String[] args) {

		char letra = 'b';
		System.out.println(letra);

		char valor = 70;
		System.out.println(valor);

		valor = (char) (valor + 1);
		System.out.println(valor);

		String frase = "Testando sobre String em ";
		System.out.println(frase);

		frase = frase + 2023;
		System.out.println(frase);
	}

}
